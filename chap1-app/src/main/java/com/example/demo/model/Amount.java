package com.example.demo.model;

public class Amount 
{
	private double amount;

	public Amount() 
	{
		super();

		
	}

	public Amount(double amount) 
	{
		super();
		this.amount = amount;
	}

	public double getAmount() 
	{
		return amount;
	}

	public void setAmount(double amount) 
	{
		this.amount = amount;
	}
	
	
}
